    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        try {
            int clave = Integer.parseInt(txtClave.getText().trim());
            if (arbolB.contains(clave)) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "La clave ya existe en el √Årbol B.",
                        "Duplicado", javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            arbolB.add(clave);
            java.util.Collections.sort(arbolB);
            areaLog.append("‚úî Clave " + clave + " insertada correctamente.\n");
            areaLog.append("Estado actual del √Årbol B: " + arbolB + "\n\n");
            txtClave.setText("");
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Ingrese un n√∫mero v√°lido.",
                    "Error de formato", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            int claveBuscada = Integer.parseInt(txtBuscar.getText().trim());
            if (arbolB.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "El √Årbol B est√° vac√≠o. Inserte claves primero.",
                        "Aviso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            areaLog.append("üîé Buscando la clave " + claveBuscada + "...\n");
            for (int clave : arbolB) {
                areaLog.append("   Visitando nodo con clave: " + clave + "\n");

                if (clave == claveBuscada) {
                    areaLog.append("‚úî Clave encontrada: " + claveBuscada + "\n\n");
                    javax.swing.JOptionPane.showMessageDialog(this,
                            "Clave " + claveBuscada + " encontrada en el √Årbol B.",
                            "Resultado", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    txtBuscar.setText("");
                    return;
                }
            }
            areaLog.append("‚ùå Clave " + claveBuscada + " no encontrada.\n\n");
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Clave no encontrada.",
                    "Resultado", javax.swing.JOptionPane.WARNING_MESSAGE);

            txtBuscar.setText("");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Ingrese un n√∫mero v√°lido para buscar.",
                    "Error de formato", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

